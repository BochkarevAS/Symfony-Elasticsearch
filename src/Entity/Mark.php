<?php

declare(strict_types=1);

namespace App\Entity;

/**
 * @ORM\Entity()
 * @ORM\Table(name="mark")
 */
class Mark
{
    /**
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="IDENTITY")
     * @ORM\Column(type="integer")
     */
    private $id;

    /**
     * @ORM\Column(type="text")
     */
    private $name;

    public function getName()
    {
        return $this->name;
    }

    public function setName($name)
    {
        $this->name = $name;
    }
}